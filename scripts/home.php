        <div class="col-md-6 order-1">
            <h2>¿Qué es un Herbario On-line?</h2>
            <p class="text-justify">
                Antes de definir qué es un <a href="https://es.wikipedia.org/wiki/Herbario_virtual" class="text-decoration-none text-success font-weight-bold">herbario on-line</a> deberíamos concretar que es un herbario. <br>
                Un herbario es una colección de plantas o partes de plantas debidamente identificadas, y que contiene información sobre las mismas, como la identidad del recolector, fecha y lugar de recolección, hábitat, nombre común, o usos de la planta.<br>
                Estas colecciones pueden representar la flora mundial, de un país, de una región, o incluso de una localidad. Además, pueden representar un grupo específico, como por ejemplo, plantas medicinales, plantas cultivadas, plantas acuáticas, plantas que crecen en condiciones estremas de temperatura, o una familia específica. <br>
                Tradicionalmente, las plantas se prensaban, secaban y conservaban en pliegos de papel. <br>
                En un herbario virtual o herbario on-line se utilizan imágenes de las plantas, presentadas en una página web.
            </p>
        </div>

        <div class="col-lg-6 order-3">
            <h2>¿Qué voy a encontrar en este Herbario On-line?</h2>
            <p  class="text-justify">En este herbario podrás encontrar plantas vasculares encontradas en Crevillent, Alicante. <br>
                El Herbario On-line se ha estructurado en fichas, o páginas propias para cada especie vegetal presente en el herbario. Cada ficha consta de imágenes de las plantas (porte general, flor, hoja y fruto), e información sobre las características diagnósticas de la planta, además de su nombre científico, en castellano, catalán e inglés, y otros datos como distribución, hábitat o su estado de conservacion.
            </p>
