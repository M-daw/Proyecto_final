            <h2>¿Qué es un Herbario?</h2>
            <p>
                Un herbario es una colección de plantas o partes de plantas debidamente identificadas, y que contiene
                información sobre las mismas, como la identidad del recolector, fecha y lugar de recolección, hábitat,
                nombre
                común, o usos de la planta.<br>

                Estas colecciones pueden representar la flora mundial, de un país, de una región, o incluso de una
                localidad.
                Además, pueden representar un grupo específico, como por ejemplo, plantas medicinales, plantas
                cultivadas,
                plantas acuáticas, plantas que crecen en condiciones estremas de temperatura, o una familia específica.
                <br>
                Tradicionalmente, las plantas se prensaban, secaban y conservaban en pliegos de papel. Para este
